JOIN LY_PESSOA P ON P.PESSOA = A.PESSOA
JOIN LY_CURSO C ON C.CURSO = A.CURSO
JOIN HD_MUNICIPIO HM ON HM.MUNICIPIO = P.END_MUNICIPIO
LEFT JOIN LY_CONTRATO_ALUNO CA ON A.ALUNO = CA.ALUNO
LEFT JOIN LY_CONTRATO_ALUNO_IMG CAI ON CAI.ID_CONTRATO_ALUNO = CA.ID_CONTRATO_ALUNO
JOIN LY_UNIDADE_FISICA UF ON UF.UNIDADE_FIS = A.UNIDADE_FISICA
LEFT JOIN VW_FTC_ALUNO_CURSO_VALOR_FAT VW ON VW.ALUNO = A.ALUNO AND VW.ANO = CA.ANO AND VW.PERIODO = CA.PERIODO
LEFT JOIN VW_FTC_ALUNO_CURSO_VALOR_FAT_HIST VWH ON VWH.ALUNO = A.ALUNO AND VWH.ANO = CA.ANO AND VWH.PERIODO = CA.PERIODO
