--PEGANDO A CH TOTAL DOS 3 PRIMEIROS SEMESTRES
SELECT A.ALUNO, T.CPF, SUM(G.CARGA_HORARIA) AS TOTAL_CARGA
--DROP TABLE #TEMP_CPF_ALUNO
INTO #TEMP_CPF_ALUNO 
FROM LY_ALUNO A
JOIN LY_PESSOA P ON P.PESSOA = A.PESSOA
JOIN LY_DISCGRADE G ON G.CURSO = A.CURSO AND G.CURRICULO = A.CURRICULO AND G.TURNO = A.TURNO
JOIN #TEMP_CPF T ON T.CPF = P.CPF
WHERE 1=1
AND A.SIT_ALUNO = 'Ativo'
AND EXISTS (SELECT 1 FROM VW_MATRICULA_E_PRE_MATRICULA WHERE ALUNO = A.ALUNO)
AND G.SERIE_IDEAL IN (1,2,3)
GROUP BY A.ALUNO, T.CPF


--PEGANDO A CARGA HORARIA CUMPRIDA NOS 3 PRIMEIROS SEMESTRES
SELECT	DISTINCT 
		  A.UNIDADE_FISICA
		, UF.NOME_COMP AS NOME_UNIDADE
		, C.TIPO AS TIPO_CURSO
		, A.CURSO
		, C.NOME AS NOME_CURSO
		, T.CPF
		, A.ALUNO
		, A.NOME_COMPL
		, SUM(G.CARGA_HORARIA) AS CH_CUMPRIDA
--DROP TABLE #TEMP_CH_CUMPRIDA
INTO #TEMP_CH_CUMPRIDA
FROM LY_ALUNO A
JOIN #TEMP_CPF_ALUNO T ON T.ALUNO = A.ALUNO
JOIN LY_CURSO C ON C.CURSO = A.CURSO
JOIN LY_UNIDADE_FISICA UF ON UF.UNIDADE_FIS = A.UNIDADE_FISICA
JOIN LY_DISCGRADE G ON G.CURSO = A.CURSO AND G.TURNO = A.TURNO AND G.CURRICULO = A.CURRICULO
JOIN LY_HISTMATRICULA HM ON HM.DISCIPLINA = G.DISCIPLINA AND HM.ALUNO = A.ALUNO
WHERE 1=1
AND G.SERIE_IDEAL IN (1,2,3)
AND HM.SITUACAO_HIST IN ('Aprovado','Dispensado')
GROUP BY A.UNIDADE_FISICA, UF.NOME_COMP, C.TIPO, A.CURSO, C.NOME, T.CPF, A.ALUNO, A.NOME_COMPL
GO

--PEGANDO A LISTA DE ALUNOS QUE CUMPRIRAM A CH DOS 3 PRIMEIROS SEMESTRES
SELECT T2.* FROM #TEMP_CPF_ALUNO T1
JOIN #TEMP_CH_CUMPRIDA T2 ON T1.CPF = T2.CPF
WHERE T1.TOTAL_CARGA <= T2.CH_CUMPRIDA