			BI.TIPO_FINAN,
			BI.FINAN,
			BI.DATA_PERIODO,
			BI.INDICE_REPROVACAO,
			BI.QTD_INADIMP,
			BI.PERC_FALTAS,
			BI.DATA_MATRICULA,
			BI.ANTECIP_FINAN,
			BI.DT_ANTECIP,
