create VIEW [dbo].[VW_FTC_RELACAO_CHEQUE] 
AS

select 

	CHE.BANCO,
	CHE.AGENCIA,
	CHE.CONTA_BANCO,
	CHE.SERIE,
	CHE.NUMERO,
	CHE.VALOR,
	CHE.VALOR_DESC,
	CHE.DT_RECEB,
	CHE.DT_PREV_DEP,
	CHE.DT_DEPOSITO,
	CHE.DT_DEVOLUCAO1,
	CHE.DT_DEVOLUCAO2,
	CHE.DT_REAPRES,
	CHE.DT_BAIXA,
	CHE.DT_CANC,
	CHE.ANO,
	CHE.MES,
	CHE.DATA_CUSTODIA,

	ALU.ALUNO,
	ALU.NOME_COMPL,
	CHE.TITULAR,
	RESP.RESP,
	CHE.CPF_TITULAR,
	CHE.CGC_TITULAR,
	ALU.UNIDADE_FISICA,
	ALU.UNIDADE_ENSINO
 
  from LY_CHEQUES CHE

	  JOIN LY_RESP_FINAN RESP
	  ON RESP.RESP = CHE.RESP

      JOIN LY_PLANO_PGTO_PERIODO PPP
      ON PPP.RESP = CHE.RESP

      JOIN LY_ALUNO ALU
      ON ALU.ALUNO = PPP.ALUNO
GO


